
// Η αφηρημένη κλάση Apps είναι μια υποκλάση της διεπαφής Software και αναπαριστά ένα λογισμικό εφαρμογών.

public abstract class Apps implements Software  {

    //Η μέθοδος price() είναι αφηρημένη και πρέπει να υλοποιηθεί από τις υποκλάσεις της κλάσης Apps.
    // Ανάλογα με την εφαρμογή, αυτή η μέθοδος θα επιστρέφει την τιμή του λογισμικού.

    @Override
    public abstract float price();

}
