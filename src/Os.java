

// Η αφαιρετική κλάση Os υλοποιεί το διεπαφή Software και αναπαριστά ένα λειτουργικό σύστημα.

public abstract class Os implements Software  {

      //  Η μέθοδος price() είναι αφαιρετική και αναμένεται να υλοποιηθεί από τις υποκλάσεις
    //  της κλάσης για να επιστρέψει την τιμή του λειτουργικού συστήματος.

    @Override
    public abstract float price();
}
